export function buildJustinoMissingCodes(): string[] {
  const codes = new Set<string>();

  for (const [prefix, numbers] of Object.entries(JUSTINO_MISSING_BY_PREFIX)) {
    for (const number of numbers) {
      codes.add(`${prefix}${number}`);
    }
  }

  return [...codes].sort((left, right) => left.localeCompare(right, undefined, { numeric: true }));
}
